public class CircleFriend {

    public String headUrl;
    public String name;
    public String title;
    public String dept;
    public String hospital;
    public String state;

    public CircleFriend(String headUrl, String name, String title, String dept, String hospital, String state) {
        this.headUrl = headUrl;
        this.name = name;
        this.title = title;
        this.dept = dept;
        this.hospital = hospital;
        this.state = state;
    }
}
